    private void btnBorrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBorrarMouseClicked
        String reporteSeleccionado = this.lstReportesGuardados.getSelectedValue();
        
        if (reporteSeleccionado == null) {
            JOptionPane.showMessageDialog(null, "Seleccione un reporte para borrar.", "ERROR: No hay seleccion de reporte", 0);
            return;
        }
        
        try {
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Estas Seguro(@) de querer borrar el reporte?", "Corfirmar respuesta", JOptionPane.YES_NO_OPTION, 3);
            if (confirmacion == JOptionPane.YES_OPTION) {
                if (archivoUtilReportes.borrarReporte(reporteSeleccionado)) {
                    JOptionPane.showMessageDialog(null, "Reporte: " + reporteSeleccionado + " eleminado exitosamente"
                            , "Exito", 1);
                }
            } else {
                
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnBorrarMouseClicked

    private void btnAbrirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAbrirMouseClicked
        String reporteSeleccionado = this.lstReportesGuardados.getSelectedValue();
        
        if (reporteSeleccionado == null) {
            JOptionPane.showMessageDialog(null, "Seleccione un reporte para abrir.", "ERROR: No hay seleccion de reporte", 0);
            return;
        }
        
        try {
            archivoUtilReportes.abrirReporte(reporteSeleccionado);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", 0);
        }
    }//GEN-LAST:event_btnAbrirMouseClicked

