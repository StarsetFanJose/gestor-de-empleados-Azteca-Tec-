    private void cargarReportesGuardados() {
        try {
            List<String> reportes = archivoUtilReportes.cargarReportesGuardados();
            for (String reporte : reportes) 
                modeloLista.addElement(reporte);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", 0);
        }
    }
    
