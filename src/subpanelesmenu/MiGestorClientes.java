    private void btnAbrirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAbrirMouseClicked
        String reporteSeleccionadoC = this.lstReportesGuardadosClientes.getSelectedValue();
        String reporteSeleccionadoAct = this.lstReportesGuardadosAct.getSelectedValue();
        
        if (reporteSeleccionadoC != null) {
            try {
                reportesClientesUtil.abrirReporte(reporteSeleccionadoC);
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", 0);
            }
        } else if (reporteSeleccionadoAct != null) {
            try {
                reportesActividadesUtil.abrirReporte(reporteSeleccionadoAct);
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", 0);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecciona un reporte para abrirlo", "ADVERTENCIA: No hay seleccion de reporte", 2);
        }
        
    }//GEN-LAST:event_btnAbrirMouseClicked

