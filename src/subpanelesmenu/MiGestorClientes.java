    private void btnBorrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBorrarMouseClicked
        String reporteSeleccionadoC = this.lstReportesGuardadosClientes.getSelectedValue();
        String reporteSeleccionadoAct = this.lstReportesGuardadosAct.getSelectedValue();
        
        if (reporteSeleccionadoC != null) {
            try {
                int confirmacion = JOptionPane.showConfirmDialog(null, "¿Estas seguro(@) de querer borrar el reportee?", "Confirmar respuesta",
                        JOptionPane.YES_NO_OPTION, 3);
                if (confirmacion == JOptionPane.YES_OPTION) {
                    if (reportesClientesUtil.borrarReporte(reporteSeleccionadoC)) {
                        JOptionPane.showMessageDialog(null, "Reporte " + reporteSeleccionadoC + " borrado exitosamente",
                                "Exito de eliminacion", 1);
                    }
                } else {
                    
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        } else if (reporteSeleccionadoAct != null) {
            try {
                int confirmacion = JOptionPane.showConfirmDialog(null, "¿Estas seguro(@) de querer borrar el reportee?", "Confirmar respuesta",
                        JOptionPane.YES_NO_OPTION, 3);
                if (confirmacion == JOptionPane.YES_OPTION) {
                    if (reportesActividadesUtil.borrarReporte(reporteSeleccionadoAct)) {
                        JOptionPane.showMessageDialog(null, "Reporte " + reporteSeleccionadoAct + " borrado exitosamente",
                                "Exito de eliminacion", 1);
                    }
                } else {
                    
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes seleccionar un reporte para borrarlo.", "ADVERTENCIA: No hay seleccion de reporte", 2);
        }
    }//GEN-LAST:event_btnBorrarMouseClicked


