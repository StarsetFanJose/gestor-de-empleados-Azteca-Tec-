    private void openMenu() {
        timer = new Timer(10, new ActionListener() {
            int width = menu.getWidth();
            @Override
            public void actionPerformed(ActionEvent e) {
                if (width < 378) {
                    width += 10;
                    menu.setBounds(0, 0, width, menu.getHeight());
                } else {
                    timer.stop();
                    isMenuOpen = true;
                }
            }
        });
        timer.start();
    }
    
    private void closeMenu() {
        timer = new Timer(10, new ActionListener() {
            int width = menu.getWidth();
            @Override
            public void actionPerformed(ActionEvent e) {
                if (width > 0) {
                    width -= 10;
                    menu.setBounds(0, 0, width, menu.getHeight());
                } else {
                    timer.stop();
                    isMenuOpen = false;
                }
            }
        });
        timer.start();
    }
    
